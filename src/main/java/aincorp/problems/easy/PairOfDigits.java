package aincorp.problems.easy;

public class PairOfDigits {

    private int arabic;
    private char roman;

    public PairOfDigits(int arabic, char roman) {
        this.arabic = arabic;
        this.roman = roman;
    }

    public int getArabic() {
        return arabic;
    }

    public char getRoman() {
        return roman;
    }
}
